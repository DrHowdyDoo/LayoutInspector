package com.drhowdydoo.layoutinspector.util;

public class Insets {

    public float left;
    public float right;
    public float top;
    public float bottom;

    public Insets(float left, float right, float top, float bottom) {
        this.left = left;
        this.right = right;
        this.top = top;
        this.bottom = bottom;
    }



}
